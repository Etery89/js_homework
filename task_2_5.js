/*
5. Реализовать основные 4 арифметические операции в виде функций с двумя параметрами. 
Обязательно использовать оператор return.
*/

function getSumTwoNums (firstNum, secondNum) {
    return firstNum + secondNum;
}

function getDifferenceTwoNums (firstNum, secondNum) {
    return firstNum - secondNum;
}

function getProductTwoNums (firstNum, secondNum) {
    return firstNum * secondNum;
}

function getQuotienTwoNums (firstNum, secondNum) {
    return firstNum / secondNum;
}

